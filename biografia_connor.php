    <p>Ratonhnhaké:ton (1756 - ¿?), conocido mayormente como Connor Kenway, 
        fue un Maestro Asesino Kanien'kehá:ka durante el período de la Guerra de la Revolución Americana, capitán de la Aquila, 
        heredero de la Hacienda Davenport y líder de la Hermandad de los Asesinos Coloniales. Nació del Templario Haytham Kenway y Kaniehtí:io,
         una mujer Nativo Americana de la villa Kanien'kehá:ka, en Kanatahséton.</p>

            <p>A una edad muy temprana cerca de sus 4 años de edad fue atacado por un grupo de Templarios cuyo objetivo era encontrar el lugar dejado por los Precursores,
